package Bierkopf;

public class P {
  /*
   * class just for simplifying output
   * 
   * System.out.println(s) -> P.pln(s)
   */

  static void pln() {
    System.out.println();
  }

  static void pln(String s) {
    System.out.println(s);
  }

  static void p(String s) {
    System.out.print(s);
  }

}
